
package com.gustavo.javabasico.aula11.handson;

public class VariaveisPontoFlutuante {
    
    public static void main(String[] args) {
        
        // Declaração de variáveis
        double valorDaPassagem = 2.90;
        float valorTomate = 3.95f; // colocar o f porque por padrão ponto literal é double
        
        //Saída dos valores
        System.out.println("Valor da passagem: " + valorDaPassagem);
        System.out.println("Valor do tomate: " + valorTomate);
    }
    
    
}
